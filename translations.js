class TranslationManager {
    constructor() {
        this.currentLang = localStorage.getItem('cronos_lang') || 'en';
    }

    init() {
        document.addEventListener('DOMContentLoaded', () => this.translatePage());
        window.addEventListener('cronosLanguageChanged', (e) => {
            this.currentLang = e.detail.language;
            this.translatePage();
        });
    }

    setLanguage(lang) {
        if (cronosTranslations[lang]) {
            this.currentLang = lang;
            localStorage.setItem('cronos_lang', lang);
            window.dispatchEvent(new CustomEvent('cronosLanguageChanged', { detail: { language: lang } }));
        }
    }

    translatePage() {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(el => {
            const key = el.getAttribute('data-i18n');
            const translation = this.getTranslation(key);
            if (translation) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    el.placeholder = translation;
                } else {
                    el.textContent = translation;
                }
            }
        });
    }

    getTranslation(key) {
        return cronosTranslations[this.currentLang]?.[key] || cronosTranslations['en']?.[key] || key;
    }
}

const cronosTranslator = new TranslationManager();
cronosTranslator.init();
window.cronosTranslator = cronosTranslator;